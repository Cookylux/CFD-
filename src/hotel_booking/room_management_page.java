    private void upDateDB() {
    String[] columnNames = {"Room Number", "Room Type", "Bed", "Price", "Status"};
    DefaultTableModel tbModel = new DefaultTableModel();
    tbModel.setColumnIdentifiers(columnNames);
    jTable1.setModel(tbModel); // Link the model to the table

    String sql = "SELECT RoomNum, RoomType, Bed, Price, Status FROM room_management";

    try (Connection sqlConn = DriverManager.getConnection(dataConn, username, password);
         PreparedStatement pst = sqlConn.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {

        while (rs.next()) {
            String roomNum = rs.getString("RoomNum");
            String roomType = rs.getString("RoomType");
            String bed = rs.getString("Bed");
            String price = rs.getString("Price");
            String status = rs.getString("Status");

            tbModel.addRow(new Object[]{roomNum, roomType, bed, price, status});
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
    }
}

