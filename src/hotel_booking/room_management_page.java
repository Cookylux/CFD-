    private void bt_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_addActionPerformed
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            sqlConn = DriverManager.getConnection(dataConn, username, password);
            pst = sqlConn.prepareStatement("insert into room_management(RoomNum, RoomType, Bed, Price, Status)values(?, ?, ?, ?, ?)");
            PreparedStatement checkExist = sqlConn.prepareStatement(
            "SELECT COUNT(*) FROM room_management WHERE RoomNum = ?");
            
            String roomNumber = txt_room_num.getText();
            
            if (checkbox_addrn.isSelected()) {
                if (roomNumber.startsWith("R") && roomNumber.length() > 1) {
                    try {
                        int number = Integer.parseInt(roomNumber.substring(1));
                        if (number >= 21 && number <= 30) {
                            // Duplicate check
                            checkExist.setString(1, roomNumber);
                            ResultSet rs = checkExist.executeQuery();
                            if (rs.next() && rs.getInt(1) > 0) {
                                JOptionPane.showMessageDialog(this, "Room number already exists in the database.");
                                return;
                            }

                            pst.setString(1, roomNumber);
                        } else {
                            JOptionPane.showMessageDialog(this, "Additional room number must be between R21 and R30.");
                            return;
                        }
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(this, "Invalid number format in room code!");
                        return;
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid room format! Must start with 'R'.");
                    return;
                }
            } else {
                // Get selected room from combo box
                String selectedRoom = cb_roomnum.getSelectedItem().toString();

                // Duplicate check
                checkExist.setString(1, selectedRoom);
                rs = checkExist.executeQuery();
                if (rs.next() && rs.getInt(1) > 0) {
                    JOptionPane.showMessageDialog(this, "Room number already exists in the database.");
                    return;
                }

                pst.setString(1, selectedRoom);
            }
            
            
            
            pst.setString(2, cb_roomtype.getSelectedItem().toString());
            pst.setString(3, txt_bed.getText());
            pst.setInt(4, Integer.parseInt(txt_price.getText()));
            pst.setString(5, cb_status.getSelectedItem().toString());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Added");
            upDateDB();
        }
        catch (ClassNotFoundException ex){
            java.util.logging.Logger.getLogger(room_management_page.class.getName()).log(java.util.logging.Level.SEVERE, null,ex);
        }catch (SQLException ex){
            java.util.logging.Logger.getLogger(room_management_page.class.getName()).log(java.util.logging.Level.SEVERE, null,ex);
        }
    }//GEN-LAST:event_bt_addActionPerformed

